import bpy

def get_language():
    """从插件设置中获取当前语言，如果没有设置则默认中文"""
    try:
        # 尝试从新包名获取
        addon_prefs = bpy.context.preferences.addons.get(__package__.split('.')[0])
        if addon_prefs and hasattr(addon_prefs.preferences, "language"):
            return addon_prefs.preferences.language
    except:
        pass
    return 'ZH'

