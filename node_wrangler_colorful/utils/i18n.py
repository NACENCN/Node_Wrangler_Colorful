def T(zh_msg, en_msg=None):
    """翻译助手函数"""
    lang = get_language()
    if lang == 'EN':
        if en_msg:
            return en_msg
        # 查字典
        entry = translations.get(zh_msg)
        if entry and "EN" in entry:
            return entry["EN"]
        return zh_msg # 没找到英文就返回中文(MsgID)
    return zh_msg
